package task2;

public class Rectangle extends Figures {
    private final double sideA;
    private final double sideB;

    public Rectangle(double sideA, double sideB) {
        this.sideA = sideA;
        this.sideB = sideB;
    }

    @Override
    public double area() {
        return sideA * sideB;
    }

    @Override
    public double volume(){
        throw new UnsupportedOperationException("Rectangle has no volume");
    }
}
